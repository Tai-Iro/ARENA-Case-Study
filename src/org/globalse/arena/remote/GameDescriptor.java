package org.globalse.arena.remote;

import java.io.Serializable;

/**
 * This class is a container for transporting information about games to and
 * from remote methods. Instances are created by the arena server and sent to the
 * match front ends. The arena server and the game peers access Game instances
 * directly, however, match front ends can only access state through a GameInfo
 * object.
 */
public class GameDescriptor implements Serializable {
	
	private String name;
	private String description;
	
	public GameDescriptor(String name, String description) {
		this.name = name;
		this.description = description;
	}
	
	public String getName() {
		return name;
	}
	
	public String getDescription() {
		return description;
	}
}

